#include <bits/stdc++.h>
using namespace std;
#define endl "\n"
void insert(int arr[], int i)
{
    for (int j = i; j > 0; j--)
    {
        if (arr[j] < arr[j - 1])
        {
            int temp = arr[j];
            arr[j] = arr[j - 1];
            arr[j - 1] = temp;
        }
    }
}
void insertionSort(int arr[], int n)
{
    for (int i = 1; i < n; i++)
    {
        insert(arr, i);
    }
}
