#include <bits/stdc++.h>
using namespace std;
#define endl "\n"
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        if (n % 2 == 0)
        {
            cout << n << " is even" << endl;
        }
        else
        {
            cout << n << " is odd" << endl;
        }
    }
    return 0;
}